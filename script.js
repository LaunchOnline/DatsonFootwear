        // Render Products
        const productList = document.getElementById("product-list");

        products.forEach((product, index) => {
            let featureList = product.features.map(feature => `<li>${feature}</li>`).join("");

            let cardHtml = `
               <div class="col-md-4 mb-4">
    <div class="card-container">
        <div class="card" id="card-${index}">
            <!-- Front Side -->
            <div class="card-front">
                <img src="${product.image}.png" class="product-image" alt="${product.article}">
                <h5>${product.article}</h5>
                <p>${product.product_type}</p>
                <button class="btn btn-primary view-details" data-index="${index}">View Details</button>
            </div>
            <!-- Back Side -->
            <div class="card-back">
                <h6>${product.brand}</h6>
                <p>Type: ${product.type}</p>
                <p>Size: ${product.size}</p>
                <p>Sole: ${product.sole}</p>
                <ul>${featureList}</ul>
                <button class="btn btn-secondary back-button" data-index="${index}">Back</button>
            </div>
        </div>
    </div>
</div>
            `;

            productList.innerHTML += cardHtml;
        });

        // Add click event to flip cards
        document.addEventListener("click", function (event) {
            if (event.target.classList.contains("view-details")) {
                let cardIndex = event.target.getAttribute("data-index");
                document.getElementById(`card-${cardIndex}`).classList.add("flipped");
            } else if (event.target.classList.contains("back-button")) {
                let cardIndex = event.target.getAttribute("data-index");
                document.getElementById(`card-${cardIndex}`).classList.remove("flipped");
            }
        });